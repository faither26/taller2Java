package calcular;

public class Masa {
    private double altura;
    private double peso;

    public Masa() {
        this.altura = 0;
        this.peso = 0;
    }

    public double getAltura() {
        return altura;
    }

    public void setAltura(double altura) {
        this.altura = altura;
    }

    public double getPeso() {
        return peso;
    }

    public void setPeso(double peso) {
        this.peso = peso;
    }
}
