package calcular;

public class Zodiaco {
    private int mes;
    private int dia;

    public Zodiaco() {
        this.mes = 0;
        this.dia = 0;
    }

    public int getMes() {
        return mes;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public int getDia() {
        return dia;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }
}
