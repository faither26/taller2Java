package calcular;

public class Vector {
    private int cantidad;
    private int pos;


    public Vector() {
        this.cantidad = 0;
        this.pos = 0;

    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }

    public int getPos() {
        return pos;
    }

    public void setPos(int pos) {
        this.pos = pos;
    }


}
